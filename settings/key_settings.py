# standard key bindings for racing mode movement

import pygame

STD_ACCEL_KEY = pygame.K_UP # Pfeil hoch = Gas
STD_BRAKE_KEY = pygame.K_DOWN # Pfeil runter = Bremse
STD_BOOST_KEY = pygame.K_SPACE # Space = Boost

STD_LEFT_KEY = pygame.K_LEFT # Pfeil links = lenken
STD_RIGHT_KEY = pygame.K_RIGHT # Pfeil rechts = lenken

STD_CONFIRM_KEY = pygame.K_SPACE # Space zum Bestätigen in Menüs
STD_DEBUG_RESTART_KEY = pygame.K_r # standard key to restart a race in debug mode